var currentQuestionIndex = 0;
var totalMarks = 0;

// Set total number of pages
document.getElementById("totalPage").innerText = allQuestions.length;

function renderQuestion() {
  var questionElement = document.getElementById("question");
  var buttonsElement = document.getElementById("buttons");
  
  var currentQuestion = allQuestions[currentQuestionIndex];
  
  // Display question
  questionElement.innerHTML = currentQuestion.question;
  
  // Display options
       buttonsElement.innerHTML = "";
  currentQuestion.options.forEach((option) => {
    buttonsElement.innerHTML += `<button onclick="checkAnswer('${option}')" class="bg-danger text-center text-light rounded me-5" style="height: 50px; width: 300px; margin:10px;">${option}</button>`;
  });

  // Update current page number
  document.getElementById("currentPage").innerText = currentQuestionIndex + 1;
}

function checkAnswer(selectedOption) {
  var correctAnswer = allQuestions[currentQuestionIndex].correctAnswer;
  if (selectedOption === correctAnswer) {
    totalMarks += 1;
  }
  nextQuestion();
  event.preventDefault();
}

function nextQuestion() {
  currentQuestionIndex++;
  if (currentQuestionIndex < allQuestions.length) {
    renderQuestion();
  } else {
    localStorage.setItem("CorrectAnswer", JSON.stringify(totalMarks));
    localStorage.setItem(
      "WrongAnswer",
      JSON.stringify(allQuestions.length - totalMarks)
    );
    localStorage.setItem(
      "Per",
      JSON.stringify((totalMarks * 100) / allQuestions.length)
    );
    location.replace("../result/Result.html");
  }
}

// Timer functionality
var min = 15;
var sec = 0;
var mili = 9;

var displayMinute = document.getElementById("minute");
var displaySecond = document.getElementById("second");
var displayMiliSecond = document.getElementById("milisecond");

function renderTimer() {
  displayMinute.innerHTML = min;
  displaySecond.innerHTML = sec < 10 ? "0" + sec : sec;
  displayMiliSecond.innerHTML = mili;
}

function timer() {
  mili--;
  if (mili < 0) {
    mili = 9;
    sec--;
    if (sec < 0) {
      sec = 59;
      min--;
      if (min == -1) {
        clearInterval(interval);
        alert("Time up");
        localStorage.setItem("CorrectAnswer", JSON.stringify(totalMarks));
        localStorage.setItem(
          "WrongAnswer",
         Math.round( JSON.stringify(allQuestions.length - totalMarks))
        );
        localStorage.setItem(
          "Per",
          JSON.stringify(Math.r((totalMarks * 100) / allQuestions.length))
        );
        location.replace("./Result.html");
      }
    }
  }

  renderTimer();
}

var interval;

function startTimer() {
  interval = setInterval(timer, 100);
}

renderTimer(); // Initial render to show the starting time
startTimer();

// Render the first question
renderQuestion();
